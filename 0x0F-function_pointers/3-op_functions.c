#include "3-calc.h"

/**
 * op_add - calculates sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of the integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of the integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two integers
 * @a: first integer
 * @b: second integer
 * Return: product of the integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Calculates the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: division of the integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Calculates the modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: modulus of two integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
